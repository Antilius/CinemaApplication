package ObjectsInCinema;

public class BookedPlace {
    
    int row;
    int place;

    public BookedPlace(int row, int place) {
        this.row = row;
        this.place = place;
    }

    public int getRow() {
        return row;
    }

    public int getPlace() {
        return place;
    }
    
    
    
}
